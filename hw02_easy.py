# потому как он быстрее вариантов через исключение
# и более универсальный чем вариант 2.1 хотя и более медленный



# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

print("изи-3.1. Новый список по условию( змеиный вариант)")
list1 = range(10, 21)


def compare1(per):
    if per % 2:
        return per * 2
    else:
        return per / 4


print(list(map(compare1, list1)))

print("изи-3.2. Новый список по условию( обычный вариант)")
list1 = list(range(10, 21))
for i in range(len(list1)):
    if list1[i] % 2:
        list1[i] *= 2
    else:
        list1[i] /= 4
print(list1)



